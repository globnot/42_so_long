#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	len;
	char	*dest;
	int		i;

	len = ft_strlen(s);
	dest = malloc(sizeof(char) * (len + 1));
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
